const labels = {
  en: "English",
  "zh-Hans": "简体中文",
  "zh-Hant": "繁體中文",
  ja: "日本語"
};

function normalizeLang(value) {
  if (!value) return "en";
  if (value.startsWith("zh-Hant")) return "zh-Hant";
  if (value.startsWith("zh")) return "zh-Hans";
  if (value.startsWith("ja")) return "ja";
  return "en";
}

function currentLang() {
  const hash = new URLSearchParams(window.location.hash.replace(/^#/, ""));
  return normalizeLang(hash.get("lang") || localStorage.getItem("lmv-lang") || navigator.language);
}

function applyLang(lang) {
  const table = messages[lang] || messages.en;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (table[key]) node.textContent = table[key];
  });
  const switcher = document.querySelector("[data-lang-switcher]");
  if (switcher) switcher.textContent = labels[lang] || labels.en;
  localStorage.setItem("lmv-lang", lang);
}

window.addEventListener("DOMContentLoaded", () => {
  applyLang(currentLang());
  const switcher = document.querySelector("[data-lang-switcher]");
  if (switcher) {
    switcher.addEventListener("click", () => {
      const keys = Object.keys(labels);
      const next = keys[(keys.indexOf(currentLang()) + 1) % keys.length];
      window.location.hash = `lang=${next}`;
      applyLang(next);
    });
  }
});

window.addEventListener("hashchange", () => applyLang(currentLang()));
